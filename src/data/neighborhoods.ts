export function computeResilienceScore(
  n: Neighborhood,
  prefs: { workLocation: string; rentBudget: number; maxCommute: number; wellnessPriorities: string[] }
): number {
  // Affordability (0-25): how well rent fits budget
  const rentRatio = Math.min(n.avg1BRRent / prefs.rentBudget, 2);
  const affordability = Math.max(0, 25 * (1 - (rentRatio - 0.5) / 1.5));

  // Commute (0-25): how well commute fits max
  const commuteKey = prefs.workLocation === "uw" ? "commuteToUW" : "commuteToDT";
  const commuteMin = n[commuteKey as keyof Neighborhood] as number;
  const commuteScore = commuteMin <= prefs.maxCommute 
    ? 25 * (1 - commuteMin / prefs.maxCommute) 
    : Math.max(0, 25 * (1 - (commuteMin - prefs.maxCommute) / 30));

  // Transit & Walkability (0-25)
  const mobilityScore = (n.transitScore * 0.4 + n.walkScore * 0.3 + n.bikeScore * 0.3) / 100 * 25;

  // Wellness (0-25): based on wellness priorities
  let wellnessBase = n.wellnessScore;
  if (prefs.wellnessPriorities.includes("parks") || prefs.wellnessPriorities.includes("running-trails")) {
    wellnessBase = (wellnessBase + n.greenSpaceScore) / 2;
  }
  const wellnessScoreVal = (wellnessBase / 100) * 25;

  const total = Math.round(affordability + commuteScore + mobilityScore + wellnessScoreVal);
  return Math.min(100, Math.max(0, total));
}
